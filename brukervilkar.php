                problemet og så vidt det er mulig angi når ytelsen kan leveres. Tilsvarende gjelder 
                hvis det må antas ytterligere forsinkelser etter at første varsel er gitt. <br><br> 

                En part kan kreve erstattet ethvert direkte tap, herunder merkostnader ved dekningskjøp, 
                tap som skyldes merarbeid og andre direkte kostnader i forbindelse med forsinkelse, 
                mangel eller annet mislighold, med mindre den misligholdende parten godtgjør at 
                misligholdet eller årsaken til misligholdet ikke skyldes den misligholdende parten. <br><br>

